package chap3;

/*
 * 대입 연산자
 * = , += , -= , *= 등등등
 * 
 * 
 * 
 */

public class OpEx9 {
	public static void main(String[] args) {
		int num = 100;
		System.out.println(num);

		num += 100;
		System.out.println(num);
		
		num -= 100;
		System.out.println(num);
		
		num *= 100;
		System.out.println(num);
		
		num /= 100;
		System.out.println(num);
	}
}
