package chap8;

abstract class Animal{
	String name;
	Animal(String name){
		this.name = name;
	}
	abstract void eat();
}

interface Flyable{
	void fly();
}

class Dove extends Animal implements Flyable{

	Dove() {
		super("비둘기");	
	}

	@Override
	public void fly() {
		System.out.println("비둘기는 날아다닌다.");
		
	}

	@Override
	void eat() {
		System.out.println("비둘기는 작은 벌레를 잡아먹는다.");
	}
	
	public String toString() {
		return "비둘기";
	}
}

class Monkey extends Animal{

	Monkey() {
		super("원숭이");
	}

	@Override
	void eat() {
		System.out.println("원숭이는 나무에서 열매를 따먹는다.");
		
	}
	
	public String toString() {
		return "원숭이";
	}
	
}

public class Exam1 {

	public static void main(String[] args) {
		Animal a = new Dove();
		a.eat();
		if(a instanceof Flyable) {
			((Flyable) a).fly();
		}
		a = new Monkey();
		a.eat();
		if(a instanceof Flyable) {
			((Flyable) a).fly();
		}else {
			System.out.println("원숭이는 날아다니지 못한다");
		}
		

	}

}
