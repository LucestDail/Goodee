package chap3;

public class Opex7 {

	public static void main(String[] args) {
		int test1 = 16<<2;
		System.out.println(test1);

		int test2 = 16>>2;
		System.out.println(test2);

		int test4 = 16>>>2;
		System.out.println(test4);
		
		int test5 = -16<<2;
		System.out.println(test5);

		int test6 = -16>>2;
		System.out.println(test6);

		int test8 = -16>>>2;
		System.out.println(test8);
	}

}
